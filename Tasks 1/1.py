'''
Question 01: Variable Mutability (Price Override)

A product's initial discounted price is 1500.
After the Black Friday sale ends, update it
to the original price of 2000 and print it.
'''

# Initial discounted price
discount_price = 1500

# Original price
original_price = 2000

# Update the price
discount_price = original_price

# Print the updated price
print("Updated Price:", discount_price)